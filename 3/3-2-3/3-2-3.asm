	mov ax,di			;Поместить в AX введенное число (число строк треугольника)
	xor ah,ah			;Обнулить старшую часть AX (т.к. введенное число от 1 до 9 и хранится в AL)
	
	mov bl,2
	div bl				;AX/BL; AL -- целое, AH -- остаток
	cmp ah,0    
